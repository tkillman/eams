package cpa.biz.ad.bi.model;

public class EntschInfoVO {
	private int cno; /** 기수 */
	private String crseCode; /** 과정코드 */
	private String edcBeginDe; /** 교육시작일 */
	private String edcEndDe; /** 교육종료일 */
	private int nmprCo; /** 인원수 */
	private String year; /** 연도 */
	private String crseNm; /** 과정명 */
	public int getCno() {
		return cno;
	}
	public void setCno(int cno) {
		this.cno = cno;
	}
	public String getCrseCode() {
		return crseCode;
	}
	public void setCrseCode(String crseCode) {
		this.crseCode = crseCode;
	}
	public String getEdcBeginDe() {
		return edcBeginDe;
	}
	public void setEdcBeginDe(String edcBeginDe) {
		this.edcBeginDe = edcBeginDe;
	}
	public String getEdcEndDe() {
		return edcEndDe;
	}
	public void setEdcEndDe(String edcEndDe) {
		this.edcEndDe = edcEndDe;
	}
	public int getNmprCo() {
		return nmprCo;
	}
	public void setNmprCo(int nmprCo) {
		this.nmprCo = nmprCo;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getCrseNm() {
		return crseNm;
	}
	public void setCrseNm(String crseNm) {
		this.crseNm = crseNm;
	}
	
	
}
